namespace Instride\Bundle\PimcoreAiBundle\DependencyInjection;

use Symfony\Component\Config\FileLocator;
use Symfony\Component\DependencyInjection\ContainerBuilder;
use Symfony\Component\DependencyInjection\Loader;
use Symfony\Component\HttpKernel\DependencyInjection\Extension;

class PimcoreAiExtension extends Extension
{
    /**
     * {@inheritdoc}
     *
     * @throws \Exception
     */
    public function load(array $configs, ContainerBuilder $container): void
    {
        $loader = new Loader\YamlFileLoader($container, new FileLocator(__DIR__ . '/../Resources/config'));
        $loader->load('services.yaml');

        $configuration = new Configuration();
        $config = $this->processConfiguration($configuration, $configs);

        // Register parameters
        foreach ($config as $configName => $aiConfig) {
            foreach ($aiConfig as $subConfigName => $confValue) {
                $container->setParameter(
                    \sprintf('pimcore_ai.%s.%s', $configName, $subConfigName),
                    $confValue
                );
            }
        }
    }
}
